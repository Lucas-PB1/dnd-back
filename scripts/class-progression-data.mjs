function slots(...values) {
  const out = {};
  for (let i = 0; i < values.length; i++) {
    const v = values[i];
    if (v != null && v !== 0) out[String(i + 1)] = v;
  }
  return out;
}

/** @param {number} level @param {number} pb @param {Record<string, unknown>} rest */
function row(level, pb, rest) {
  return { level, proficiencyBonus: pb, ...rest };
}

